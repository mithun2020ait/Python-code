# append()
a = [1, 2, 3]
print(a)

a.append(4)
print(a)

#insert()
a.insert(1, 5)
print(a)

#sort()
s = [5, 6, 8, 7, 1, 3, 2]
s.sort()
print(s)

#pop()
a.pop(0)
print(a)

#clear()
list = [2,5,8,9,3]
list.clear()
print(list)

#reverse()
a.reverse()
print(a)

fruits = ['Apple', 'Banana', 'Kiwi', 'Kiwi']

#index()
print(fruits.index('Apple'))

#count()
print(fruits.count('Kiwi'))